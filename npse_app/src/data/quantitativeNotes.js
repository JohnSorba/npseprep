// Helper functions
export const getQuantitativeSubjectById = (subjectId) => {
    return quantitativeSubjects.find(subject => subject.id === subjectId);
};

export const getQuantitativeUnitById = (subjectId, unitId) => {
    const subject = getQuantitativeSubjectById(subjectId);
    if (!subject) return null;
    return subject.units.find(unit => unit.id === unitId);
};
