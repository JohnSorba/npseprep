// Helper functions
export const getVerbalSubjectById = (subjectId) => {
    return verbalSubjects.find(subject => subject.id === subjectId);
};

export const getVerbalUnitById = (subjectId, unitId) => {
    const subject = getVerbalSubjectById(subjectId);
    if (!subject) return null;
    return subject.units.find(unit => unit.id === unitId);
};
