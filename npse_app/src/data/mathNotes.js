// Helper functions
export const getMathSubjectById = (subjectId) => {
    return mathSubjects.find(subject => subject.id === subjectId);
};

export const getMathUnitById = (subjectId, unitId) => {
    const subject = getMathSubjectById(subjectId);
    if (!subject) return null;
    return subject.units.find(unit => unit.id === unitId);
};
