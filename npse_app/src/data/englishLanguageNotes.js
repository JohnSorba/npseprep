export const getEnglishSubjectById = (subjectId) => {
    return englishLanguageSubjects.find(subject => subject.id === subjectId) || null;
};

export const getEnglishUnitById = (subjectId, unitId) => {
    const subject = getEnglishSubjectById(subjectId);
    if (!subject) return null;
    return subject.units.find(unit => unit.id === unitId) || null;
};

// Helper to get total unit count
export const getTotalEnglishUnits = () => {
    return englishLanguageSubjects.reduce((total, subject) => total + subject.unitCount, 0);
};
